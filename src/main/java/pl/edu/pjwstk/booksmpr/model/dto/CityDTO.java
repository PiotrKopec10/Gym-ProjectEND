package pl.edu.pjwstk.booksmpr.model.dto;

public class CityDTO {
    private String id;
    private String name;
    private String latitude;
    private String longitude;


    public String getId() {
        return id;
    }


    public String getName() {
        return name;
    }


    public String getLatitude() {
        return latitude;
    }


    public String getLongitude() {
        return longitude;
    }

}
